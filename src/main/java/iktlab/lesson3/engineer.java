package iktlab.lesson3;

public class engineer {

    private String name;
    private String surname;
    private int age;
    private double salary;
    private double bonus;

    public String getName() {
        return name;
    }

    public String getSurname() {
        return surname;
    }

    public int getAge() {
        return age;
    }

    public double getSalary() {
        return salary;
    }

    public double getBonus() {
        return bonus;
    }
}
